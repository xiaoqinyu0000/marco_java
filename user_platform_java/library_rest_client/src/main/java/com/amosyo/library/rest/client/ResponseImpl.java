    @Override
    public <T> T readEntity(Class<T> aClass) {
        final String rspStr = ThrowableCallable.call(() -> requireNonNull(this.mResponse.body()).string(), false, null);
        if (String.class.equals(aClass)) {
            return (T) rspStr;
        }
        final ObjectMapper objectMapper = new ObjectMapper();
        return ThrowableCallable.call(() -> objectMapper.readValue(rspStr, aClass), false, null);
    }

